Warriors Academy established in year 2016 with the mission of promotion of martial arts and fitness at Chhattisgarh.
It is the only affiliated unit of CGMMA and was aimed at providing standardized training for various martial arts
disciplines at Chhattisgarh first . Understanding the need of a career oriented approach, Academy keep on upgrading its
athletes, officials coaches and trainers to be in pace with growing world outside. Continuous educational programs on
anti-doping code, group seminars and workshops to upgrade the athlete's skills and the know hows of how to face the
competitions at various levels, Warriors Academy work tirelessly for creating champions, who can represent India at
international platforms.
Warriors Academy provide an in-class training background for all passionate sports persons, starting from as early as 5
years of age. We understand that building a strong athlete requires training at grassroot level, starting from his/her
fitness level, diet, sleep pattern,apt equipments, targetted training sessions, recovery and rest pre,post and during
championships, a strong team of professionals like coaches, Physics,Nutritionists ,Strength &conditioning
Coaches,fitness trainers etc. work on an athlete's development.We at academy look into all these parameters which
required for an athlete's growth. At Warriors Academy we understand the multiprong approach from development of youth as
a strong, confident and fit athlete, and an upgraded strong coach and official.This is the reason why athletes from
Warriors Academy are among the top placement on leaderboard of champions from India in mixed martial arts.
Warriors Academy specialises in MMA (Mixed Martial Arts) Sports . We provide an updated and in-class training session
for athletes interested in taking their careers ahead as a MMA fighter, having a dream and passion to represent India at
international platform and earning pride for nation.
Warriors Academy athletes has created history at international platform (World Youth and Asian MMA Championships) and
